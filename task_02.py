# 2. Дан список повторяющихся элементов. Вернуть список с дублирующимися элементами.
# В результирующем списке не должно быть дубликатов.

lst = [1, 2, 2, 3, 3, 4, 4, 4, 5, 5, 6, 6, 6, 7, 7, 8, 8, 8, 8, 9, 9, 9, 10, 10, 10, 11, 11, 12, 13, 14, 15, 16, 17]
print(lst)

COUNT = 2

my_list = list()
for i in set(lst):
    if lst.count(i) == COUNT:
        my_list.append(i)

print(my_list)